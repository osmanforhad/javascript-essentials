/**
 * Arithmetic operators
 */

// + (Addition)                            
var result = 1 + 2; // 3

// – (Subtraction)
var result = 3 - 2; // 1

// * (Multiplication)
var result = 2 * 3; // 6

// / (Division)
var result = 8 / 4; // 2

// % (Modulo)
var result = 8 / 5; // 3

// + (Unary plus)
var result = +"3.4"; // 3.4
 
var result2 = +false; // 0
 
var result3 = +"abc"; // NaN

// – (Unary negation)
var x = 6;
 
var result = -x; // -6
 
var result = -"def"; // NaN

// ++ (Increment)
var x = 2;
 
var y = ++x; // y = 3, x = 3
 
var t = 2;
 
var u = t++; // u = 2, t = 3

// — (Decrement)
var x = 2;
 
var y = --x; // y = 1, x = 1
 
var t = 2;
 
var u = t--; // u = 2, t = 1

// = (Assignment operator)
x = 2;
